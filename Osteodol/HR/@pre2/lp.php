						<p>Posljednju godinu sam hodao sa štapom. Prije sljedećeg sistematskog pregleda, dao sam otkaz. Ionako nisam mogao proći na sistematskom.</p>
						<h1> Kako sam se izliječio </h1>
						<p>Iz mog bogatog iskustva sa bolovima u zglobovima, mogu reći da liječnici znaju samo za kondrosamin. Možda još ponekad propišu hidrokortizon ili diprospan.</p>
						<p><b> Ovo NE POMAŽE! </b>
						</p>
						<p>Još gore, stalne <a href="#form-wrap"> injekcije diklofenaka </a> koje sam morao primati da olakšam bol, uzrokovale su bolove u stomaku. Morao sam dobijati bizmut da se spriječi oštećenje želuca.</p>
						<p>Dok sam tražio odista djelotvoran preparat, imao sam 4 punkcije za uklanjanje tečnosti. Posljednji put kad sam to radio, pojavio se gnoj. Liječnik je rekao da je vrlo izvijesno da ću ubrzo morati na operaciju, ugraditi endoprotezu. Htjeli su staviti dijelove od titanijuma umjesto mog propalog koljena. Počeo sam to izučavati. Kako se ispostavilo, čak i ako ugradnja proteze prođe dobro, još uvijek bih morao hodati sa štapom. Sama operacija košta najmanje 24,000 kuna. To je cijena njemačke proteze, naše su jeftinije 6,000 kuna, ali je rizik od komplikacija mnogo veći. Ukratko, odlučio sam pričekati i dobro razmisliti.</p>
