						<p>To zapravo dugujem svojoj ženi. Imala je školsku drugaricu koja se udala za jednog vijećnika. On je patio zbog bola u koljenu kao i ja. Možda ne baš toliko.</p>
						<p>Jednom je moja žena hodala ulicom kad se pored nje zaustavio automobil. Ovaj vijećnik je izašao iz automobila i odskakutao do dućana.</p>
