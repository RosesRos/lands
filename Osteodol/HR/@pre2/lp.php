						<p>Imam 62 godine, umirovljenik sam. Prije mirovine, radio sam na visokim pećima u tvornici metala. U početku sam bio samo obični radnik, onda sam promijenio mjesto i počeo raditi kao inženjer zaštite na radu. Tko se razumije u visoke peći zna koliko se to razlikuje od posla koji obavlja obrađivač metala. Oni samo obrađuju čelik u kalupima, sve što rade je da sjede i pritiskaju ručice i dugmiće. Mi smo sve radili na teži način. Ako je trebalo, radili smo sa lopatom i željeznim polugama. Bilo je nevjerojatno vruće pored peći, a nije se moglo pobjeći od stalnog propuha. Tamo sam uništio koljena i donji dio kralježnice.</p>
